function add(num1: number, num2: number): number {
    return num1 + num2;
};

console.log(add(1, 6));

const sub = (num1: number, num2: number): number => num1 - num2;

console.log(sub(1, 6));

const multi = function (num1: number, num2: number): number {
    return num1 * num2
};

console.log(multi(5, 6));